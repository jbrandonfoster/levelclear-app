export function getDayContent(dayNumber: number): DayContent | null {
  return allDays.find((day) => day.dayNumber === dayNumber) || null;
}

export function getCurrentPhase(dayNumber: number): 'THE DECISION' | 'THE VALLEY' | 'THE SHIFT' | 'THE FOUNDATION' | 'PLUS ONE' {
  if (dayNumber <= 15) return 'THE DECISION';
  if (dayNumber <= 30) return 'THE VALLEY';
  if (dayNumber <= 45) return 'THE SHIFT';
  if (dayNumber <= 60) return 'THE FOUNDATION';
  return 'PLUS ONE';
}

export function getPhasePercentComplete(dayNumber: number): number {
  const phaseStart = dayNumber <= 15 ? 1 : dayNumber <= 30 ? 16 : dayNumber <= 45 ? 31 : dayNumber <= 60 ? 46 : 61;
  const phaseEnd = dayNumber <= 15 ? 15 : dayNumber <= 30 ? 30 : dayNumber <= 45 ? 45 : dayNumber <= 60 ? 60 : 365;
  const daysInPhase = phaseEnd - phaseStart + 1;
  const daysCompleted = dayNumber - phaseStart + 1;
  return Math.min(100, Math.round((daysCompleted / daysInPhase) * 100));
}

export function getOverallPercentComplete(dayNumber: number): number {
  return Math.min(100, Math.round((dayNumber / 60) * 100));
}
